                    <h3>Seconda giornata di campionato: ottimi Comets!</h3>
                    <div class="data">2011-11-19</div>
                    <p>
                        Gli incontri della seconda giornata, per i Comets, ospitati da Rovello e Gerenzano, sono stati giocati in settimana, anzich&egrave; di domenica (come da calendario); la prima partita, giocata marted&igrave; 25 Ottobre, ha visto i Saronno Castor scendere in campo con i Sisma Salus di Gerenzano, ancora stremati dall'influenza, che hanno dovuto combattere in assoluta inferiorit&agrave; numerica. I cinque giocatori Sisma non si sono dati per vinti, e sono scesi in campo molto determinati: il gioco &egrave; stato comunque abbastanza fluido nonostante il risultato della partita fosse gi&agrave; scritto dal primo minuto. Il 37 a 76 finale dimostra infatti come la squadra di casa sia comunque riuscita a costruire un buon attacco, nonostante la netta disparit&agrave; di livello e di numero di giocatori in campo.
                        Gioved&igrave; 27 invece si &egrave; svolto l'incontro tra i Rovello Sgavisc ed i Saronno Pollux. Nonostante i pronostici fossero assolutamente a favore dei padroni di casa, gli Sgavisc, il pubblico presente, seppur di modeste dimensioni, ha dimostrato che le sorprese sarebbero potute arrivare proprio nel palazzetto di Via Madonna. I Pollux sono scesi in campo pi&ugrave; determinati che mai e, complice la poco brillante prestazione dei rovellesi, sono riusciti a rimontare un parziale di dieci punti a loro sfavore nell'ultimo tempo, perdendo la partita per un soffio, proprio agli ultimi secondi: il risultato finale vede gli Sgavisc avanti di un solo punto, 63 a 62, e dei Pollux raggianti per essersi avvicinati cos&igrave; tanto ad una squadra che promette di arrivare molto in alto questa stagione.<br />
                        I Pollux si sono ripetuti nel match con Gerenzano, giocato il primo novembre: avanti sin dal primo minuto, hanno mantenuto un buon vantaggio per tutta la partita, concludendo con un 56 a 43 sui Sisma, i quali finalmente hanno giocato in 7, senza cambi ma senza nessun infortunato in campo.<br />
                        L' ultima partita di questa seconda giornata, tra le lombarde, &egrave; stata la pi&ugrave; attesa: Sgavisc-Castor. I rovellesi, carichi e intenzionati a dimenticare la partita vinta per un soffio con gli altri Comets, sono stati accolti da un pubblico numeroso, ma sugli spalti vi erano anche diversi tifosi saronnesi. Il match &egrave; stato piuttosto equilibrato per il primo tempo, con i Castor avanti di qualche punto, e degli Sgavisc molto pi&ugrave; in forma rispetto alla partita precedente; i saronnesi hanno per&ograve; mantenuto il vantaggio anche per il secondo tempo, mentre la squadra di casa si &egrave; lasciata andare a qualche errore. Il terzo tempo &egrave; stato prevalentemente dei Castor, che hanno concluso vincendo per 66 a 52. In questa giornata di campionato intanto anche i Varese Pirates esordiscono nella massima serie, con una buona prestazione tra l'altro, a Ferrara. Questa giornata ha segnato a riposo le formazioni astigiane, Atomik ed EleKTrauti.<br />
                        La prossima giornata sar&agrave; di riposo per i saronnesi, mentre Asti ospiter&agrave; Ferrara e Varese incontrer&agrave; Rovello e Gerenzano.<br />
                    </p>
