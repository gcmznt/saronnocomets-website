                    <h2>Curiosit&agrave;</h2>
                    <h3>Castor e Pollux</h3>
                    <p>
                        La costellazione dei Gemelli rappresenta i gemelli Castore e Polluce; ai Greci essi erano noti come i Dioscuri,
                        che letteralmente significa &laquo;figli di Zeus&raquo;. I mitologi, per&ograve;, non furono tutti d'accordo sul fatto che fossero
                        entrambi veramente figli di Zeus, a causa delle insolite circostanze della loro nascita. La loro madre era Leda,
                        Regina di Sparta, alla quale fece un giorno visita Zeus, sottoforma di cigno (rappresentato nella costellazione
                        del Cigno). Quella stessa notte Leda giacque anche con il marito, il Re Tindaro. Entrambe le unioni furono allietate
                        da prole, poich&eacute; in seguito Leda diede alla luce quattro bambini. Secondo la versione pi&ugrave; comunemente accettata,
                        Polluce ed Elena (la futura famosa Elena di Troia) erano figli di Zeus, e quindi immortali, mentre Castore e Clitennestra
                        erano figli di Tindaro, e quindi erano mortali.
                    </p>
                    <p>
                        <img src="_static/img/castor-pollux.png" class="shadow" />
                    </p>
                    <p>
                        Castore e Polluce crebbero molto legati l'uno all'altro, non litigarono mai n&eacute; mai agirono senza prima consultarsi.
                        Si diceva che si assomigliassero molto fisicamente e che persino si vestissero allo stesso modo, come spesso fanno
                        i gemelli. Castore fu un famoso cavaliere e guerriero, mentre Polluce fu un campione di pugilato.
                    </p>
                    <p>
                        Gli inseparabili gemelli si unirono alla spedizione di Giasone e degli Argonauti alla ricerca del vello d'oro.
                    </p>
                    <p>
                        Castore e Polluce si scontrarono con un'altra coppia di gemelli, Ida e Linceo, a proposito di due belle donne. Ida
                        e Linceo (che erano anche membri dell'equipaggio della nave Argo) erano fidanzati con Febe e Ilaria, ma Castore e
                        Polluce gliele rubarono. Ida e Linceo li inseguirono e le due coppie di gemelli si batterono. Castore fu trafitto
                        da una spada sguainata da Linceo, che fu subito ucciso da Polluce. Ida attacc&ograve; Polluce ma fu respinto da una folgore di Zeus.
                    </p>
                    <p>
                        Polluce pianse il suo fratello morto e chiese a Zeus di concedere a entrambi l'immortalit&agrave;. Zeus li sistem&ograve; insieme
                        in cielo come la costellazione dei Gemelli, dove sono raffigurati abbracciati, inseparabili per l'eternit&agrave;.
                    </p>
                    <p>
                        Le due stelle pi&ugrave; brillanti della costellazione dei Gemelli si chiamano Castore e Polluce e indicano i punti
                        delle teste dei gemelli. Gli astronomi hanno scoperto che in realt&agrave; Castore &egrave; un sistema di sei stelle tenute
                        insieme dalla forza di gravit&agrave;. Polluce &egrave; una stella gigante arancione.<br />
                    </p>
                    <h3>Mizar e Polaris</h3>
                    <p>
                        Mizar &egrave; una stella nella costellazione dell'Orsa Maggiore. Il nome viene dall'arabo e significa &quot;velo&quot;. All'indagine astronomica si rivela un sistema stellare composto da un totale di sei stelle.
                        Mizar &egrave; ben visibile ad occhio nudo. Le persone di buona vista possono notare una debole stella compagna appena ad est, chiamata Alcor oppure 80 Ursae Majoris. Le due sono a volte chiamate rispettivamente il Cavallo e il Cavaliere da una traduzione dalla lingua persiana, e l'abilit&agrave; nel vederle entrambe &egrave; un test tradizionale della vista (usato gi&agrave; in antichit&agrave;: era una delle prove attitudinali per le aspiranti guardie dell'imperatore nei paesi arabi).
                    </p>
                    <p>
                        Polaris &egrave; un sistema stellare triplo situato nella costellazione dell'Orsa Minore; &egrave; la stella pi&ugrave; luminosa della costellazione, nonch&eacute; la brillante stella vicina al polo nord celeste, motivo per cui &egrave; anche conosciuta come stella polare o stella del nord.<br />
                        Polaris &egrave; apparentemente ferma nel cielo, mentre tutte le altre stelle sembrano ruotarle attorno. &Egrave; cos&igrave; un ottimo punto di riferimento per la navigazione celeste.<br />
                    </p>
                    <h3>Rigel, Altair, Aldebaran e Antares</h3>
                    <p>
                        Aldebaran &egrave; la stella pi&ugrave; brillante della costellazione del Toro. Il suo nome deriva dalla parola araba al-Dabaran,
                        &quot;l'inseguitore&quot;, in riferimento al modo in cui la stella sembra seguire l'ammasso delle Pleiadi nel loro moto notturno.<br />
                        Astrologicamente, Aldebaran era una stella fortunata, che portava ricchezze ed onori. Era una delle quattro &quot;stelle reali&quot;
                        dei Persiani dal 3000 AC.
                    </p>
                    <p>
                        Altair &egrave; la dodicesima stella pi&ugrave; brillante del cielo, ed una delle pi&ugrave; vicine a noi. &Egrave; anche uno dei vertici del Triangolo Estivo.<br />
                        Altair &egrave; degna di nota per la sua rotazione su se stessa, molto rapida: misurando la larghezza delle sue linee spettrali,
                        si &egrave; trovato che il suo equatore compie una rotazione completa in appena 6 ore e mezzo. Per confronto, il nostro Sole impiega
                        circa 25 giorni per ruotare su s&eacute; stesso.<br />
                        Il nome Altair deriva dalle parole arabe al-nasr-al-ta'ir, che significano l'aquila &quot;volante&quot;.<br />
                        Questa stella era portatrice di sventura in astrologia, significando pericolo da parte di rettili.
                    </p>
                    <p>
                        Antares (detta anche Alpha Scorpii o Calbalacrab) &egrave; una supergigante rossa situata nella costellazione dello Scorpione.<br />
                        Il suo nome deriva dal greco e significa rivale di Ares (anti-Ares) o &quot;simile ad Ares&quot;, probabilmente a causa del colore
                        rossastro simile all'aspetto del pianeta Marte. &Egrave; anche nota come cuore dello Scorpione, data la sua posizione nella
                        costellazione e il suo colore.<br />
                        Il suo colore distintivo ne ha fatto un oggetto di grande interesse per molti popoli nella storia. Molti dei templi
                        egizi erano orientati con Antares, la stella era una delle quattro &quot;stelle regali&quot; dei Persiani (insieme a Aldebaran, Regolo
                        e Fomalhaut).
                    </p>
                    <p>
                        Rigel, Beta Orionis, &egrave; la settima stella pi&ugrave; luminosa del cielo. &Egrave; di gran lunga la stella pi&ugrave; luminosa della nostra zona
                        della Via Lattea.<br />
                        Rigel &egrave; una stella tripla. Attorno alla principale si trovano le due compagne Rigel B e Rigel C, che orbitano l'una attorno
                        all'altra, e orbitano quindi assieme attorno a Rigel.<br />
                        Il nome della stella deriva dalla sua posizione di &quot;piede&quot; sinistro di Orione. &Egrave; una contrazione di &quot;Rijl jawza al-yusra&quot;,
                        espressione araba per &quot;il piede sinistro di Colui che &egrave; Centrale&quot;.<br />
                    </p>
